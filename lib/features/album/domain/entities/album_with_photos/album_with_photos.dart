class AlbumWithPhoto {
  final int albumId;
  final int id;
  final String title;
  final int photoId;
  final String photoTitle;
  final String url;
  final String thumbnailUrl;

  AlbumWithPhoto({
    required this.albumId,
    required this.id,
    required this.title,
    required this.photoId,
    required this.photoTitle,
    required this.url,
    required this.thumbnailUrl,
  });
}
